// Input two floating-point numbers and print their product.

#include<stdio.h>
int main () {
    float a,b;
    printf("Enter the first number:");
    scanf("%f",  &a);
    printf("Enter the second number:");
    scanf("%f", &b);
    printf("The product of two number is %0.2f\n",a*b);
    return 0;
}
 
